const Shorthand = require('server/util/shorthand');

// example usage:
// Area.Portland.key = "P" - sent b/t client and server.
// Area.Portland.label = "Portland" - used for display and logging.
// Area.Portland.value = "Portland" - what it looks like in the database.
class Area {
  static Portland  = Shorthand.Enum('Portland');
  static Vancouver = Shorthand.Enum('Vancouver');
  static Westside  = Shorthand.Enum('Westside');
  static Eastside  = Shorthand.Enum('Eastside');
  static Clackamas = Shorthand.Enum('Clackamas');

  // ex. transform 'P' into "Portland"
  static keyToValue(key) { return Shorthand.keyToValue(Area, key) };
}

class Audience {
  static General = Shorthand.Enum('General', "General Audience");
  static Family  = Shorthand.Enum('Family');
  static Adult   = Shorthand.Enum('Adult');

  // ex. transform 'G' into "General"
  static keyToValue(key) { return Shorthand.keyToValue(Audience, key) };
}

// the status of a single instance of an event on a given day.
// generated by the daily_events view for backwards compatibility with existing queries.
class EventStatus {
  static Active    = new Shorthand('A', 'Active', 1);
  static Cancelled = new Shorthand('C', 'Cancelled', 0);

  // transform 'A' (active) into 1, and 'C' (cancelled) into 0
  // if neither, returns undefined
  static keyToValue(key) { return Shorthand.keyToValue(EventStatus, key) };
}

class Distance {
  // an array of common ride distances
  // example:
  // [0].key = "0-3"
  // [0].label = "0-3 miles"
  // [0].value = "0-3"
  static shorthands = [
    new Shorthand('0-3', '0-3 miles'),
    new Shorthand('3-8', '3-8 miles'),
    new Shorthand('8-15','8-15 miles'),
    new Shorthand('15+', '15+ miles'),
  ];

  // transforms '0-3' into '0-3'
  // returning undefined if the key was unknown.
  // ie. works as a validator
  static keyToValue(key) {
    const at = this.shorthands.findIndex(n => n.key === key);
    return at < 0 ? undefined : this.shorthands[at].value;
  }
}

